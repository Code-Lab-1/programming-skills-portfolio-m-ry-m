places=['Saudi Arabia','Sawat,Pakistan','Korea','Turkey','Japan']
print(places)

print("Sorted:")
print(sorted(places))

print("List:",places)

print("Reverse Order:")
print(sorted(places, reverse=True))

print("List:",places)

print("Reversed:")
places.reverse()
print(places)

print("Sorted in Alphabetical order:")
places.sort()
print(places)

print("Reversed:")
places.sort(reverse=True)
print(places)
